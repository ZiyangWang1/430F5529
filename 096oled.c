void OledWriteByte(uchar wbyte,uchar cmd)
{
  uchar i;

  if(cmd)
  {
    P1OUT |= PX_2_Location;
  }
  else
  {
    P1OUT &= ~PX_2_Location;
  }
  
  for(i=0;i<8;i++)
  {
    P1OUT &= ~PX_5_Location;
    if(wbyte&0x80)
    {
      P1OUT |= PX_4_Location;
    }
    else
    {
      P1OUT &= ~PX_4_Location;
    }
    P1OUT |= PX_5_Location;
    wbyte<<=1;
  }
}

